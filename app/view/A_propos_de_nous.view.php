<h2>Nos valeurs</h2>
<p>
Le partage et la convivialité restent les maîtres-mots de Draft que ce soit en face-à-face ou au travers de nos réseaux sociaux, il reste essentiel pour nous de pouvoir vous faire découvrir nos produits dans le respect et la créativité.
 
 Faites exploser vos couleurs ! 
 Exprimer votre personnalité en toute liberté avec du dynamisme. Pourquoi ne pas choisir une couleur qui reflète votre personnalité à vous et pas celle  des autres. Venez exprimer votre créativité, vos émotions, votre imagination sur le mur de notre brasserie, réservé pour vous. 
 Nous aimerons vous transmettre le goût de la bière et le savoir-faire qui est caché derrière cette production artisanale.
 
 Pour cela, nous souhaitons vous immerger dans la création de nos bières, créées dans le respect de l’écologie et de la production responsable, notamment à l’aide de produits 100 % naturels et de nos emballages recyclés certifiés “eco-friendly” . 
 Vous serez accompagnés  par une équipe dynamique qui vous diffusera de la joie et de la bonne humeur lors de votre dégustation, tout en respectant la consommation responsable et la prévention de l’alcool. 
 
 Nous sommes ouverts aux retours de nos consommateurs pour pouvoir améliorer et partager sur le sujet qui nous réunit. Afin de vous satisfaire un maximum lors de votre dégustation au sein de notre brasserie ou de votre commande.
